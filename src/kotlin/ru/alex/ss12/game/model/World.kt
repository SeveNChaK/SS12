    fun tickMonster() {
        when (monster.makeMove().direction) {
            MoveDirection.UP -> monster.y = monster.y - 1
            MoveDirection.DOWN -> monster.y = monster.y + 1
            MoveDirection.LEFT -> monster.x = monster.x - 1
            MoveDirection.RIGHT -> monster.x = monster.x + 1
            else -> return
        }
    }

    fun moveUser(user: User, direction: MoveDirection): MoveResult {
        val moveResult = MoveResult()

        val isCanMove = when (direction) {
            MoveDirection.UP -> map[user.y - 1][user.x] != 1
            MoveDirection.DOWN -> map[user.y + 1][user.x] != 1
            MoveDirection.LEFT -> map[user.y][user.x - 1] != 1
            MoveDirection.RIGHT -> map[user.y][user.x + 1] != 1
            else -> return MoveResult(isMoved = false)
        } and user.isMoveEnabled()

        if (isCanMove) {
            user.move(direction)
            moveResult.isMoved = true

            val userPoint = Point(user.x, user.y)
            val item = items[userPoint]
            if (item != null && !user.items.contains(item.type)){
                user.addItem(item)
                items.remove(userPoint)
                moveResult.isPickedUpItem = true
            }
        }

        return moveResult
    }

    private fun initItems(): MutableMap<Point, Item> {
        val result = mutableMapOf<Point, Item>()
        itemsSpawnCoordinates.forEach {
            val item = when (Item.Type.values().random()) {
                Item.Type.LAMP -> LampItem(it.x, it.y)
                Item.Type.RADIO -> RadioItem(it.x, it.y)
            }
            result[it] = item
        }
        return result
    }

    fun cellOn(x: Int, y: Int) : Cell {
        //TODO может быть что-то кроме 0 и 1
        //Out of bounds
        if (y > map.size || y < 0 || x > map[0].size || x < 0)
            return Cell.WALL
        for (user in userRepository.getUsers()){
//            if (user.x == x && user.y == y && user.alive)
            if (user.x == x && user.y == y)
                return Cell.HUMAN
        }
        if (map[y][x] == 1)
            return Cell.WALL
        return Cell.EMPTY
    }

    fun cellOn(point: Point) : Cell {
        return cellOn(point.x, point.y)
    }

    enum class Cell {
        WALL,
        EMPTY,
        HUMAN
    }

    interface UserRepository {
        fun getUsers(): Collection<Point>
    }

